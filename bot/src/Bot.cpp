#include "Bot.hpp"

/* ************************************************************************** */
/*                               ORTHODOX CLASS                               */
/* ************************************************************************** */
Bot::Bot(int port, std::string pw) : port_(port), pw_(pw) {}

Bot::Bot(void) {}

Bot::Bot(const Bot &rhs)
{
	*this = rhs;
}

Bot::~Bot(void) {}

/* ************************************************************************** */
/*                               METHODS                                      */
/* ************************************************************************** */
Bot &Bot::getInstance(int port, const std::string &pw) {
	static Bot instance(port, pw);
	return instance;
}
	
